const finnstance = document.getElementById("finn_battlestance");


finnstance.addEventListener("mouseover", event =>{

    finn_battlestance.src = `./src//pngs/finn-smiling.png`;
    finn_battlestance.alt = `smiling-fin`

    //dapat bumabalik si finn sa dati
})

finnstance.addEventListener("mouseout", event =>{

    finn_battlestance.src = `./src//pngs/finn_battlestance.png`;
    finn_battlestance.alt = `finn_battlestance`;

    //dapat bumabalik si finn sa dati
})